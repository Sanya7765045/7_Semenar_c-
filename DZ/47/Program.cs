using System;
using static System.Console;

// функция для создания и заполнения двумерного массива случайными числами
// принимает на вход количество строк и столбцов массива, а также минимальное и максимальное значения для случайных чисел
// возвращает заполненный массив
double[,] GetArray(int rows, int columns, double minValue, double maxValue)
{
    // создаем пустой двумерный массив заданной размерности
    double[,] array = new double[rows, columns];
    Random random = new Random();

    // заполняем массив случайными числами в заданном диапазоне
    // используем два вложенных цикла для перебора всех элементов массива
    for (int i = 0; i < rows; i++)
    {
        for (int j = 0; j < columns; j++)
        {
            // генерируем случайное число в заданном диапазоне и сохраняем его в текущий элемент массива
            array[i, j] = minValue + (maxValue - minValue) * random.NextDouble();

            // выводим значение текущего элемента массива на консоль
            Write($"{array[i, j]} ");
        }

        // переходим на новую строку, чтобы разделить значения элементов массива по строкам
        WriteLine();
    }

    // возвращаем заполненный массив
    return array;
}

// очищаем консоль, чтобы пользователь мог вводить данные
Clear();

// запрашиваем у пользователя количество строк для массива
Write("Введите количество строк массива: ");
int rows = int.Parse(ReadLine());

// запрашиваем у пользователя количество столбцов для массива
Write("Введите количество столбцов массива: ");
int columns = int.Parse(ReadLine());

// создаем и заполняем двумерный массив с помощью функции GetArray
// используем значения, введенные пользователем, как параметры для функции
// сохраняем результат в переменную array
double[,] array = GetArray(rows, columns, 0, 10);